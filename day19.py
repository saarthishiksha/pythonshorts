### 1. List Create Karna
fruits = ["Apple", "Banana", "Cherry"]
### 2. List Access Karna
print(fruits[0])  # Apple
print(fruits[1])  # Banana
print(fruits[2])  # Cherry
### 3. List Mein Item Append Karna
fruits.append("Dragonfruit")
print(fruits)
### 4. List Mein Item Remove Karna
fruits.remove("Banana") 
print(fruits)
### 5. List Mein Item Insert Karna
fruits.insert(1, "Blueberry")
print(fruits)
### 6. List Mein Item Count Karna
print(fruits.count("Apple"))    
### 7. List Mein Item Index Pata Karna
print(fruits.index("Cherry"))
### 8. List Length Pata Karna
print(len(fruits))
### 9. List Sort Karna
fruits.sort()
print(fruits)