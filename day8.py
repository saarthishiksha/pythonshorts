result=5+3#**Addition (+)**.  # This code adds two numbers, 5 and 3, and stores the result in the variable 'result'.
print(result)#**Output**: 8
result = 10 - 4  #**Subtraction (-)**.  # This code subtracts 4 from 10 and stores the result in the variable 'result'.
print(result)  #**Output**: 6
result = 7 * 2#**Multiplication (*)**.  # This code multiplies 7 by 2 and stores the result in the variable 'result'.
print(result)#**Output**: 14
result = 20 / 5  # **Division (/)**.  # This code divides 20 by 5 and stores the result in the variable 'result'.
print(result)  # **Output**: 4.0
result = 9 // 2  # **Floor Division (//)**.  # This code performs floor division of 9 by 2 and stores the result in the variable 'result'.
print(result)  # **Output**: 4  
result = 2 ** 3  # **Exponentiation (**)**.  # This code raises 2 to the power of 3 and stores the result in the variable 'result'.
print(result)  # **Output**: 8  
result = 10 % 3  # **Modulus (%)**.  # This code calculates the remainder of 10 divided by 3 and stores the result in the variable 'result'.
print(result)  # **Output**: 1